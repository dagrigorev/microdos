#include "text_console.hpp"

namespace microdos::platform {

// Compact pseudo-font: not pretty, but independent from firmware/EDK2.
// It draws recognizable block glyphs for early boot diagnostics.
static uint8_t glyphRow(char c, uint32_t row) {
    if (c == ' ') return 0x00;
    if (c >= '0' && c <= '9') {
        static const uint8_t digits[10][8] = {
            {0x3c,0x66,0x6e,0x76,0x66,0x66,0x3c,0x00}, {0x18,0x38,0x18,0x18,0x18,0x18,0x7e,0x00},
            {0x3c,0x66,0x06,0x1c,0x30,0x66,0x7e,0x00}, {0x3c,0x66,0x06,0x1c,0x06,0x66,0x3c,0x00},
            {0x0c,0x1c,0x3c,0x6c,0x7e,0x0c,0x0c,0x00}, {0x7e,0x60,0x7c,0x06,0x06,0x66,0x3c,0x00},
            {0x1c,0x30,0x60,0x7c,0x66,0x66,0x3c,0x00}, {0x7e,0x66,0x06,0x0c,0x18,0x18,0x18,0x00},
            {0x3c,0x66,0x66,0x3c,0x66,0x66,0x3c,0x00}, {0x3c,0x66,0x66,0x3e,0x06,0x0c,0x38,0x00}
        };
        return digits[c - '0'][row];
    }
    if (c >= 'a' && c <= 'z') c = static_cast<char>(c - 'a' + 'A');
    switch (c) {
        case 'A': { static const uint8_t g[8]={0x18,0x3c,0x66,0x66,0x7e,0x66,0x66,0}; return g[row]; }
        case 'B': { static const uint8_t g[8]={0x7c,0x66,0x66,0x7c,0x66,0x66,0x7c,0}; return g[row]; }
        case 'C': { static const uint8_t g[8]={0x3c,0x66,0x60,0x60,0x60,0x66,0x3c,0}; return g[row]; }
        case 'D': { static const uint8_t g[8]={0x78,0x6c,0x66,0x66,0x66,0x6c,0x78,0}; return g[row]; }
        case 'E': { static const uint8_t g[8]={0x7e,0x60,0x60,0x7c,0x60,0x60,0x7e,0}; return g[row]; }
        case 'F': { static const uint8_t g[8]={0x7e,0x60,0x60,0x7c,0x60,0x60,0x60,0}; return g[row]; }
        case 'G': { static const uint8_t g[8]={0x3c,0x66,0x60,0x6e,0x66,0x66,0x3c,0}; return g[row]; }
        case 'H': { static const uint8_t g[8]={0x66,0x66,0x66,0x7e,0x66,0x66,0x66,0}; return g[row]; }
        case 'I': { static const uint8_t g[8]={0x7e,0x18,0x18,0x18,0x18,0x18,0x7e,0}; return g[row]; }
        case 'K': { static const uint8_t g[8]={0x66,0x6c,0x78,0x70,0x78,0x6c,0x66,0}; return g[row]; }
        case 'L': { static const uint8_t g[8]={0x60,0x60,0x60,0x60,0x60,0x60,0x7e,0}; return g[row]; }
        case 'M': { static const uint8_t g[8]={0x63,0x77,0x7f,0x6b,0x63,0x63,0x63,0}; return g[row]; }
        case 'N': { static const uint8_t g[8]={0x66,0x76,0x7e,0x7e,0x6e,0x66,0x66,0}; return g[row]; }
        case 'O': { static const uint8_t g[8]={0x3c,0x66,0x66,0x66,0x66,0x66,0x3c,0}; return g[row]; }
        case 'P': { static const uint8_t g[8]={0x7c,0x66,0x66,0x7c,0x60,0x60,0x60,0}; return g[row]; }
        case 'R': { static const uint8_t g[8]={0x7c,0x66,0x66,0x7c,0x78,0x6c,0x66,0}; return g[row]; }
        case 'S': { static const uint8_t g[8]={0x3c,0x66,0x60,0x3c,0x06,0x66,0x3c,0}; return g[row]; }
        case 'T': { static const uint8_t g[8]={0x7e,0x18,0x18,0x18,0x18,0x18,0x18,0}; return g[row]; }
        case 'U': { static const uint8_t g[8]={0x66,0x66,0x66,0x66,0x66,0x66,0x3c,0}; return g[row]; }
        case 'V': { static const uint8_t g[8]={0x66,0x66,0x66,0x66,0x66,0x3c,0x18,0}; return g[row]; }
        case 'Y': { static const uint8_t g[8]={0x66,0x66,0x3c,0x18,0x18,0x18,0x18,0}; return g[row]; }
        case ':': { static const uint8_t g[8]={0,0x18,0x18,0,0,0x18,0x18,0}; return g[row]; }
        case '[': { static const uint8_t g[8]={0x3c,0x30,0x30,0x30,0x30,0x30,0x3c,0}; return g[row]; }
        case ']': { static const uint8_t g[8]={0x3c,0x0c,0x0c,0x0c,0x0c,0x0c,0x3c,0}; return g[row]; }
        case '>': { static const uint8_t g[8]={0x60,0x30,0x18,0x0c,0x18,0x30,0x60,0}; return g[row]; }
        case '.': { static const uint8_t g[8]={0,0,0,0,0,0x18,0x18,0}; return g[row]; }
        case '-': { static const uint8_t g[8]={0,0,0,0x7e,0,0,0,0}; return g[row]; }
        default:  { static const uint8_t g[8]={0x7e,0x42,0x5a,0x5a,0x5a,0x42,0x7e,0}; return g[row]; }
    }
}

void TextConsole::initialize(Framebuffer* framebuffer) {
    framebuffer_ = framebuffer;
    clear();
}

void TextConsole::clear() {
    cursor_x_ = 0;
    cursor_y_ = 0;
    if (framebuffer_) framebuffer_->clear(bg_);
}

void TextConsole::write(const char* text) {
    if (!text) return;
    while (*text) putChar(*text++);
}

void TextConsole::writeLine(const char* text) {
    write(text);
    newline();
}

void TextConsole::putChar(char c) {
    if (!framebuffer_ || !framebuffer_->isReady()) return;
    if (c == '\n') { newline(); return; }
    if (c == '\r') return;
    drawGlyph(c, cursor_x_ * GlyphW, cursor_y_ * GlyphH);
    ++cursor_x_;
    if ((cursor_x_ + 1) * GlyphW >= framebuffer_->width()) newline();
}

void TextConsole::newline() {
    cursor_x_ = 0;
    ++cursor_y_;
    if ((cursor_y_ + 1) * GlyphH >= framebuffer_->height()) {
        clear();
    }
}

void TextConsole::drawGlyph(char c, uint32_t px, uint32_t py) {
    for (uint32_t row = 0; row < GlyphH; ++row) {
        uint8_t bits = glyphRow(c, row);
        for (uint32_t col = 0; col < GlyphW; ++col) {
            framebuffer_->putPixel(px + col, py + row, (bits & (0x80u >> col)) ? fg_ : bg_);
        }
    }
}

} // namespace microdos::platform
